public class For_Loop {
    public static void main(String[] args) {

        // for loop = executes a block of code for a limited amount of time.

        for (int i = 1; i<=10; i++) {
            System.out.println(i);
        }
        System.out.println();

        for (int j = 10; j>=0;) {
            System.out.println(j);
            j -= 1;
        }

        System.out.println("Happy New Year!");

    }
}
